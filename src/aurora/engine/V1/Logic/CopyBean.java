public class CopyBean {

    private Object object;

    public CopyBean(Object objectToCopy) {
        this.object = objectToCopy;
    }

    /**
     * @return the object
     */
    public Object getObject() {
        return object;
    }

    /**
     * @param object the object to set
     */
    public void setObject(Object object) {
        this.object = object;
    }

}
